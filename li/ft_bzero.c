#include "libft.h"

char	*ft_bzero(void *s, size_t len)
{
	size_t			i;
	unsigned char	*s1;

	i = 0;
	s1 = (unsigned char *) s;
	while (i < len)
	{
		s1[i] = '\0';
		i ++;
	}
	return (s);
}
