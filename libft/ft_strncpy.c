#include "libft.h"

char	*ft_strncpy(char *dest, char *src, size_t n)
{
	size_t	i;

	i = 0;
	while (dest[i] != '\0' && (i + 1) < n)
	{
		dest[i] = src[i];
		i++;
	}
	return (dest);
}
